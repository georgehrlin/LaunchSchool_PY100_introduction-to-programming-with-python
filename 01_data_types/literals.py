# "A literal is any syntactic notation that lets you directly represent an
# object in source code.

'Hello, world!'  # str literal
3.1415926        # float literal
True             # bool literal
{'a': 1, 'b': 2} # dict literal
[1, 2, 3]        # list literal
(4, 5, 6)        # tuple literal
{7, 8, 9}        # set literal

range(10)         # Range of numebrs: 0-9
range(1, 11)      # Range of numbers: 1-10
set()             # Empty set
frozenset([1, 2]) # Frozen set of values: 1 and 2